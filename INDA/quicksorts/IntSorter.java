/**
 * Interface IntSorter - Interface that describes a class containing one sort method
 * to sort an array.
 *
 * Created by Pontus on 01/03/2017.
 */
public interface IntSorter {

    /**
     * Sorts the array into ascending numerical order.
     * @param v the array to be sorted
     */
    void sort(int[] v);
}
